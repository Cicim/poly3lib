use bin16_macro::bin16;

fn main() {
    println!("0b{:016b}", bin16!("001{5}11{4}10", 0, 0));
    println!("0b{:016b}", bin16!("001{5}11{4}10", 15, 0));
    println!("0b{:016b}", bin16!("001{5}11{4}10", 0, 7));
    println!("0b{:016b}", bin16!("001{5}11{4}10", 15, 7));
    // bin16!("0000{8}111{}", 1, false);
}
